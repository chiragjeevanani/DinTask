import Subscription from "../Models/SubscriptionModel.js";
import Manager from "../Models/ManagerModel.js";
import Admin from "../Models/AdminModel.js";
import Task from "../Models/TaskModel.js"; // Agar tasks tracked hain
import Plan from "../Models/PlanModel.js";
import Employee from "../Models/EmployeeModel.js";
import dayjs from "dayjs";

export const getAdminDashboard = async (req, res) => {
  try {
    const adminId = req.user.id;
    const todayStart = dayjs().startOf("day").toDate();
    const todayEnd = dayjs().endOf("day").toDate();

    // üîπ Manager & Employee count
    const managerCount = await Manager.countDocuments({
      adminId,
      isDeleted: false,
    });

    const employeeCount = await Employee.countDocuments({
      adminId,
      isDeleted: false,
    });

    // üîπ Tasks summary
    const totalTasks = await Task.countDocuments({ adminId });
    const completedTasks = await Task.countDocuments({ adminId, status: "completed" });
    const pendingTasks = await Task.countDocuments({ adminId, status: "pending" });
    const activeTasks = await Task.countDocuments({ adminId, status: "active" });

    const taskCompletionPercentage = totalTasks > 0 ? Math.round((completedTasks / totalTasks) * 100) : 0;

    // üîπ Today's tasks
    const todaysCompleted = await Task.countDocuments({
      adminId,
      status: "completed",
      updatedAt: { $gte: todayStart, $lte: todayEnd },
    });

    const todaysPending = await Task.countDocuments({
      adminId,
      status: "pending",
      updatedAt: { $gte: todayStart, $lte: todayEnd },
    });

    // üîπ Tasks completed in last 7 days (day-wise)
    const weekStart = dayjs().startOf("week"); // Sunday
    const weekTasks = [];
    for (let i = 0; i < 7; i++) {
      const dayStart = weekStart.add(i, "day").startOf("day").toDate();
      const dayEnd = weekStart.add(i, "day").endOf("day").toDate();

      const completed = await Task.countDocuments({
        adminId,
        status: "completed",
        updatedAt: { $gte: dayStart, $lte: dayEnd },
      });

      weekTasks.push({
        day: dayjs(dayStart).format("dddd"), // Monday, Tuesday...
        completedTasks: completed,
      });
    }

    res.status(200).json({
      success: true,
      message: "Admin dashboard fetched successfully",
      data: {
        managerCount,
        employeeCount,
        totalTasks,
        completedTasks,
        pendingTasks,
        activeTasks,
        taskCompletionPercentage,
        todaysTasks: {
          completed: todaysCompleted,
          pending: todaysPending,
        },
        weeklyTasks: weekTasks,
      },
    });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};

export const getSuperadminDashboard = async (req, res) => {
  try {
    // üîπ Total admins
    const totalAdmins = await Admin.countDocuments({ isDeleted: false });

    // üîπ Total active subscriptions
    const totalActiveSubscriptions = await Subscription.countDocuments({
      status: "active",
      isDeleted: false,
    });

    // üîπ Total managers
    const totalManagers = await Manager.countDocuments({ isDeleted: false });

    // üîπ Plan wise revenue & admin count (include all plans)
    const allPlans = await Plan.find({ isDeleted: false }).lean();

    const planRevenueAgg = await Promise.all(
      allPlans.map(async (plan) => {
        const activeSubs = await Subscription.find({
          planId: plan._id,
          status: "active",
          isDeleted: false,
        });

        const totalRevenue = activeSubs.reduce(
          (sum, sub) => sum + (sub.finalPayableAmount || 0),
          0,
        );

        const adminCount = activeSubs.length;

        return {
          _id: plan._id,
          planName: plan.planName,
          planPrice: plan.planPrice,
          planTier: plan.planTier,
          totalRevenue,
          adminCount,
        };
      }),
    );

    // üîπ Recent 5 companies
    const recentAdmins = await Admin.find({ isDeleted: false })
      .sort({ createdAt: -1 })
      .limit(5)
      .lean();

    const recentCompanies = await Promise.all(
      recentAdmins.map(async (admin) => {
        const activeSub = await Subscription.findOne({
          adminId: admin._id,
          status: "active",
          isDeleted: false,
        }).populate("planId");

        // Total tasks & completed tasks for this admin
        const totalTasks = await Task.countDocuments({ adminId: admin._id });
        const completedTasks = await Task.countDocuments({
          adminId: admin._id,
          status: "completed",
        });

        const taskCompletionPercentage =
          totalTasks > 0 ? Math.round((completedTasks / totalTasks) * 100) : 0;

        return {
          companyName: admin.companyName,
          ownerName: admin.ownerName,
          currentPlan: activeSub?.planId?.planName || "No Active Plan",
          joinedAt: admin.createdAt,
          status: activeSub?.status || "No Active Plan",
          totalTasks,
          taskCompletionPercentage,
        };
      }),
    );

    // üîπ All tasks summary across all admins
    const totalTasksAll = await Task.countDocuments({});
    const completedTasksAll = await Task.countDocuments({
      status: "completed",
    });
    const taskCompletionAll =
      totalTasksAll > 0
        ? Math.round((completedTasksAll / totalTasksAll) * 100)
        : 0;

    res.status(200).json({
      success: true,
      message: "Superadmin dashboard fetched successfully",
      data: {
        totalAdmins,
        totalActiveSubscriptions,
        totalManagers,
        allTasksSummary: {
          totalTasksAll,
          completedTasksAll,
          taskCompletionAll,
        },
        planRevenueAgg,
        recentCompanies,
      },
    });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};


export const managerDashboard = async (req, res) => {
  try {
    const managerId = req.user.id;

    // 1Ô∏è‚É£ Get team members
    const teamMembers = await Employee.find({
      assignManagerId: managerId,
      isDeleted: false
    }).select("fullName email avatar isActive");

    const teamMemberIds = teamMembers.map(emp => emp._id);

    // 2Ô∏è‚É£ Tasks directly assigned to this manager
    const managerTasks = await Task.find({
      assignManagerId: managerId,
      isDeleted: false
    }).sort({ createdAt: -1 });

    const myTaskCount = managerTasks.length;
    const pendingTaskCount = managerTasks.filter(t => t.status === "pending").length;

    // 3Ô∏è‚É£ Team tasks
    const teamTasks = await Task.find({
      assignEmployees: { $in: teamMemberIds },
      isDeleted: false
    }).sort({ completedAt: -1 });

    const completedTeamTasks = teamTasks.filter(t => t.status === "completed").length;
    const teamProgress = teamTasks.length > 0 
      ? Math.round((completedTeamTasks / teamTasks.length) * 100)
      : 0;

    // 4Ô∏è‚É£ Recent 5 completed tasks with employee details
    const recentTasks = await Promise.all(
      teamTasks
        .filter(t => t.status === "completed")
        .sort((a, b) => new Date(b.completedAt) - new Date(a.completedAt))
        .slice(0, 5)
        .map(async (task) => {
          // fetch employee details
          const employeesCompleted = await Employee.find({
            _id: { $in: task.assignEmployees }
          }).select("fullName email avatar role roleName");

          return {
            taskId: task._id,
            title: task.title,
            completedAt: task.completedAt,
            completedBy: employeesCompleted
          };
        })
    );

    // 5Ô∏è‚É£ Response
    res.status(200).json({
      success: true,
      message: "Manager dashboard fetched successfully",
      data: {
        myTaskCount,
        pendingTaskCount,
        totalTeamMemberCount: teamMembers.length,
        teamProgress, // %
        recentTasks
      }
    });

  } catch (error) {
    console.log(error);
    res.status(500).json({ success: false, message: error.message, data: null });
  }
};
